    private void search_btn_heartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_search_btn_heartMouseClicked
        java.sql.PreparedStatement st;
        java.sql.ResultSet rs;
        
        String title = "<html><p>*The Suggested Hospital is Specialized In Deceases Related To Heart</p></html>";
        title_dec.setText(title);
        
        Boolean a= true;
        
        
        String currentlocn = loc_lbl.getSelectedItem().toString();
        test1.setText(currentlocn);
        
        String query = "SELECT Clinic_name, Clinic_add FROM `clinincs` WHERE `Location` = ? AND `spec_in` = 'Heart'";
        
        try {
            st = My_CNX3.getConnection().prepareStatement(query);
            st.setString(1, currentlocn);;
            rs = st.executeQuery();
            
            if(rs.next())
            {
                container.removeAll();
                container.repaint();
                container.revalidate();
                container.add(clinics);
                container.repaint();
                container.revalidate();
                
                String clini_name = rs.getString(1);
                String clini_add = rs.getString(2);
                
                String hospnm = "<html><p>"+ clini_name +"</p></html>";
                String hospadd = "<html><p>"+ clini_add +"</p></html>";
                
                hospiname_lbl_1.setText(hospnm);
                hospiaddress_lbl.setText(hospadd);
            }
        } catch (SQLException ex) {
            Logger.getLogger(main_form.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_search_btn_heartMouseClicked

